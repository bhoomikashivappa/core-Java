class Watch {

private String brand;
private String type;
float price;
boolean isWaterResistant;
String strapMaterial;

Watch () {
}

public void setBrand(String wBrand){
brand = wBrand;
}

public String getBrand(){
return brand;
}

public void setType(String wType){
type = wType;
}

public String getType(){
return type;
}

public void setPrice(float wPrice){
price = wPrice;
}

public float getPrice(){
return price;
}

public void setIsWaterResistant(boolean  wIsWaterResistant){
 isWaterResistant = wIsWaterResistant;
}

public boolean getIsWaterResistant(){
return  isWaterResistant;
}

public void setStrapMaterial (String wStrapMaterial){
strapMaterial = wStrapMaterial;
}

public String getStrapMaterial(){
return strapMaterial ;
}

}

