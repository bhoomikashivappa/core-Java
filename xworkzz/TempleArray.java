class TempleArray {
	private String name;
	private String addres;
	private int step;
	
	public void setName(String name){
		this.name=name;
	}
	public String getName(){
		return name;
	}
	public void setAddres(String addres){
		this.addres=addres;
	}
	public String getAddres(){
		return addres;
	}
	public void setStep(int step){
		this.step=step;
	}
	public int getStep(){
		return step;
	}

}