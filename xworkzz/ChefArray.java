class ChefArray {
    private String name ;
    private char skillLevel ;
    private String signatureDish ;
    
	public void setName(String name){
		this.name=name;
	}
	public void setSkillLevel (char skillLevel){
		this.skillLevel=skillLevel;
	}
	public void setSignatureDish(String signatureDish){
		this.signatureDish=signatureDish;
	}
	public String getName(){
		return name;
	}
	public char getSkillLevel(){
		return skillLevel;
	}
	public String getSignatureDish(){
		return signatureDish;
	}

    
	
}