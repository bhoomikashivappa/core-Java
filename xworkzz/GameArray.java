class GameArray {
	private int level;
	private String name;
	private float rating;
	
	public void setLevel(int level){
		this.level=level;
	}
	public void setName(String name){
		this.name=name;
	}
	public void setRating(float rating){
		this.rating=rating;
	}
	public int getLevel(){
		return level;
	}
	public String getName(){
		return name;
	}
	public float getRating(){
		return rating;
	}
}