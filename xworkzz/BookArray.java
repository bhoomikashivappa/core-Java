class BookArray{
	private String title;
    private String author;
    private int pages;
	
	public void setTitle(String title){
		this.title=title;
	}
	public void setAuthor(String author){
		this.author=author;
	}
	public void setPages(int pages){
		this.pages=pages;
	}
	public String getTitle(){
		return title;
	}
	public String getAuthor(){
		return author;
	}
	public int getPages(){
		return pages;
	}
	
}