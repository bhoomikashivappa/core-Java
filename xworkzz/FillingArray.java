class FillingArray {
    String name ;
    char level ; 
    String taste ;
     
    
	public void setName(String name){
		this.name=name;
	}
	public void setLevel(char level){
		this.level=level;
	}
	public void setTaste(String taste){
		this.taste=taste;
	}
	public String getName(){
		return name;
	}
	public char getLevel(){
		return level;
	}
	public String getTaste(){
		return taste;
	}
}